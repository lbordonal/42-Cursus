#include "../include/philosophers.h"

int	ft_isdigit(int c)
{
	if (c >= 48 && c <= 57)
		return (1);
	else
		return (0);
}

int	ft_atoi(char *str)
{
	int	i;
	int	result;

	i = 0;
	result = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	if (result < 0 || ((str[i] < '0' || str[i] > '9') && str[i] != '\0')
		|| result > INT_MAX)
		return (0);
	return (result);
}
